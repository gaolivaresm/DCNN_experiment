# --- Dual System Chat Function ---
# This function orchestrates the interaction between the static and dynamic models.
def chat_dual_system(prompt_text: str, max_new_tokens: int = 30, temperature: float = 0.8, beta: float = 0.3):
    """
    Generates text using a dual-system approach: a static GPT-2 (Cortex) and a dynamic DCNN (Hippocampus).
    
    Args:
        prompt_text    : The initial text prompt.
        max_new_tokens : Maximum number of tokens to generate.
        temperature    : Sampling temperature for text generation (controls randomness).
        beta           : The weighting factor for the DCNN's influence.
                         0.0 = Pure static GPT-2 (boring).
                         1.0 = Pure chaotic DCNN (unstable).
                         0.3 = A balanced point (Prigogine's sweet spot).
    """
    optimizer._prev_A.clear() # Clear previous attention cache for a fresh start (amnesia)
    
    print(f"\n[Human]: {prompt_text}")
    print("[DUAL]: ", end="", flush=True)
    
    input_ids = tokenizer.encode(prompt_text, return_tensors='pt').to(device)
    model_dynamic.train() # Dynamic model is in training mode to allow weight updates
    
    for step in range(max_new_tokens):
        optimizer.zero_grad() # Clear gradients before each optimization step
        
        # --- DUAL PREHENSION (Information Processing) ---
        # 1. Static LLM (Cortex) calculates the 'grammatical norm' (without gradient computation)
        with torch.no_grad():
            out_static = model_static(input_ids, use_cache=False)
            logits_static = out_static.logits[:, -1, :] # Logits for the next token from static model
            
        # 2. Dynamic DCNN (Hippocampus) calculates 'novelty' based on its altered state
        out_dynamic = model_dynamic(input_ids, output_attentions=True, use_cache=False)
        logits_dynamic = out_dynamic.logits[:, -1, :] # Logits for the next token from dynamic model
        
        # --- COUPLING (The Hybrid Equation) ---
        # Combine the predictions from both models. The static model provides grounding,
        # preventing the dynamic model from generating completely nonsensical words.
        logits_final = (1.0 - beta) * logits_static + (beta) * logits_dynamic
        
        # Soft repetition penalty: slightly dampens logits of already generated tokens
        for token_id in set(input_ids[0].tolist()):
            if logits_final[0, token_id] > 0:
                logits_final[0, token_id] /= 1.2
            else:
                logits_final[0, token_id] *= 1.2
                
        # --- Token Generation ---
        logits_final = logits_final / temperature # Apply temperature for sampling diversity
        probs = F.softmax(logits_final, dim=-1) # Convert logits to probabilities
        next_token = torch.multinomial(probs, num_samples=1) # Sample the next token
        
        word = tokenizer.decode(next_token[0]) # Decode the token to a human-readable word
        print(f"{word}", end=" ⏳ ", flush=True) # Print with a delay indicator
        
        # --- DCNN METABOLISM (Weight Update) ---
        # The DCNN experiences the entropy of the FINAL decision; only its weights are updated.
        # This loss drives the DCNN's learning and adaptation.
        loss = F.cross_entropy(logits_dynamic, next_token.squeeze(0)) # Compute loss for the dynamic model
        loss.backward() # Backpropagate the gradients
        
        # Compute raw novelty signal from dynamic model's attentions
        raw_novelty = optimizer.compute_novelty(out_dynamic.attentions)
        # Apply a hyperbolic tangent function to normalize novelty (prevents extreme values)
        safe_novelty = np.tanh(raw_novelty / 10.0) 
        optimizer.step(novelty_signal=safe_novelty) # Apply the Prigogine optimization step
        
        input_ids = torch.cat([input_ids, next_token], dim=-1) # Add the new token to the input sequence
        
        print("\b\b\b   \b\b\b", end="", flush=True) # Clear the delay indicator
        if next_token.item() == tokenizer.eos_token_id: # Stop if end-of-sequence token is generated
            break
            
    print("\n" + "─"*50)

