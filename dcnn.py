    # 3. Entrenar brevemente (El flujo termodinámico)
    model_trial.train()
    total_loss = 0.0

    for text in calibration_texts:
        enc = tokenizer(text, return_tensors='pt', truncation=True,
                        max_length=64, padding='max_length').to(device)

        optimizer_trial.zero_grad()
        out = model_trial(**enc, output_attentions=True)

        # Loss proxy
        loss = out.last_hidden_state[:, 0, :].norm()
        loss.backward()

        # FIX TÉRMICO: Aplicamos una función Tanh para limitar la novedad extrema
        # Evita que un pico de 500 destruya los gradientes. Lo confina entre 0 y 1.
        raw_novelty = optimizer_trial.compute_novelty()
        safe_novelty = np.tanh(raw_novelty / 10.0)

        optimizer_trial.step(novelty_signal=safe_novelty)
        total_loss += loss.item()

    # Limpiar hooks para no saturar memoria
    for h in hooks:
        h.remove()

    # 4. Medir el Colapso Modal (Similaridad)
    model_trial.eval()
    embeddings = []
    with torch.no_grad():
        for text in calibration_texts:
            enc = tokenizer(text, return_tensors='pt', truncation=True,
                            max_length=64, padding='max_length').to(device)
            out = model_trial(**enc, output_attentions=True)
