"""**Step 2: The Dual-Core Inference** (The "Brain" Architecture)
To avoid total semantic collapse (Entropy death), we implement a Cortex-Hippocampus dual system.

Static Model (Cortex): A frozen GPT-2 that provides the "Eternal Laws" of grammar.

Dynamic Model (Hippocampus): Our DCNN that "experiences" and "mutates" with every token.
"""

import time
import torch
import torch.nn.functional as F
import numpy as np
from transformers import GPT2LMHeadModel, GPT2Tokenizer

device = "cuda" if torch.cuda.is_available() else "cpu"
tokenizer = GPT2Tokenizer.from_pretrained('gpt2')

print("── Inicializando Sistema Dual ──")
# 1. LA CORTEZA (El LLM Estático / La Física Fundamental)
model_static = GPT2LMHeadModel.from_pretrained('gpt2').to(device)
model_static.eval() # Modo evaluación estricto
for p in model_static.parameters():
    p.requires_grad = False

# 2. EL HIPOCAMPO (La DCNN Termodinámica / El Fluido)
model_dynamic = GPT2LMHeadModel.from_pretrained(
    'gpt2', output_attentions=True, attn_implementation="eager"
).to(device)

# Congelamos embeddings de la DCNN (Objetos Eternos)
for p in model_dynamic.transformer.wte.parameters(): p.requires_grad = False
for p in model_dynamic.transformer.wpe.parameters(): p.requires_grad = False

optimizer = PrigogineOptimizerCausal(
    filter(lambda p: p.requires_grad, model_dynamic.parameters())
)

def chat_dual_system(prompt_text: str, max_new_tokens: int = 30, temperature: float = 0.8, beta: float = 0.3):
    """
    beta: El peso de la DCNN.
    0.0 = GPT-2 normal aburrido.
    1.0 = Caos total (como el intento anterior).
    0.3 = El punto de equilibrio (Prigogine).
    """
    optimizer._prev_A.clear() # Amnesia de la matriz de atención previa

    print(f"\n[Humano]: {prompt_text}")
    print("[DUAL]: ", end="", flush=True)

    input_ids = tokenizer.encode(prompt_text, return_tensors='pt').to(device)
    model_dynamic.train()

    for step in range(max_new_tokens):
        optimizer.zero_grad()

        # --- PREHENSIÓN DUAL ---
        # 1. El LLM Estático calcula el "Deber Ser" gramatical (sin gastar memoria en gradientes)
        with torch.no_grad():
            out_static = model_static(input_ids, use_cache=False)
            logits_static = out_static.logits[:, -1, :]

        # 2. La DCNN calcula la "Novedad" basada en su estado alterado
        out_dynamic = model_dynamic(input_ids, output_attentions=True, use_cache=False)
        logits_dynamic = out_dynamic.logits[:, -1, :]

        # --- ACOPLAMIENTO (La Ecuación Híbrida) ---
        # Combinamos los universos. El modelo estático veta palabras como "OAJOMAM".
        logits_final = (1.0 - beta) * logits_static + (beta) * logits_dynamic

        # Amortiguador suave de repetición sobre los logits fusionados
        for token_id in set(input_ids[0].tolist()):
            if logits_final[0, token_id] > 0:
                logits_final[0, token_id] /= 1.2
            else:
                logits_final[0, token_id] *= 1.2

        # Generación
        logits_final = logits_final / temperature
        probs = F.softmax(logits_final, dim=-1)
        next_token = torch.multinomial(probs, num_samples=1)

        word = tokenizer.decode(next_token[0])
        print(f"{word}", end=" ⏳ ", flush=True)

        # --- METABOLISMO DE LA DCNN ---
        # La DCNN sufre la entropía de la decisión FINAL, pero solo ella se actualiza
        loss = F.cross_entropy(logits_dynamic, next_token.squeeze(0))
        loss.backward()

        raw_novelty = optimizer.compute_novelty(out_dynamic.attentions)
        safe_novelty = np.tanh(raw_novelty / 10.0)
        optimizer.step(novelty_signal=safe_novelty)

        input_ids = torch.cat([input_ids, next_token], dim=-1)

        print("\b\b\b   \b\b\b", end="", flush=True)
        if next_token.item() == tokenizer.eos_token_id:
            break

    print("\n" + "─"*50)

