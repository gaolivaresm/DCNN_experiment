class PrigogineOptimizerCausal(Optimizer):
    """Optimizador termodinámico adaptado para modelos causales (GPT-2) sin hooks."""
    def __init__(self, params, lr=2e-5, gamma=1e-6, alpha=1.58e-4, lambda_diff=1.12e-3):
        defaults = dict(lr=lr, gamma=gamma, alpha=alpha, lambda_diff=lambda_diff)
        super().__init__(params, defaults)
        self._prev_A = {}

    def compute_novelty(self, attentions, eps=1e-8) -> float:
        kl_total = 0.0
        count = 0
        L_list = []

        # FIX 1: Proteger contra matrices vacías
        valid_attentions = [a for a in attentions if a is not None]
        if not valid_attentions:
            return 0.0

        for idx, attn in enumerate(valid_attentions):
            # attn shape: (batch, heads, seq, seq)
            A_curr = attn.detach().mean(dim=(0, 1)) # Promedio batch y heads

            # Construir Laplaciano temporal
            A_sym = 0.5 * (A_curr + A_curr.T)
            D = torch.diag(A_sym.sum(dim=1))
            L_list.append(D - A_sym)

            if idx in self._prev_A:
                # Alinear tamaños temporales (la secuencia crece 1 token por paso)
                seq_len = self._prev_A[idx].shape[0]
                A_c_cut = A_curr[:seq_len, :seq_len].clamp(min=eps)
                A_p = self._prev_A[idx].clamp(min=eps)

                kl_total += (A_c_cut * (A_c_cut / A_p).log()).sum().item()
                count += 1

            self._prev_A[idx] = A_curr

        self.L_mean = torch.stack(L_list).mean(dim=0) if L_list else None
        return kl_total / max(count, 1)

    @staticmethod
    def _semantic_diffusion(W, L, lambda_diff):
        try:
            U, S, Vh = torch.linalg.svd(W, full_matrices=False)
        except:
            return torch.zeros_like(W)
        r_eff = min(S.shape[0], L.shape[0])
        U_r, S_r, Vh_r = U[:, :r_eff], S[:r_eff], Vh[:r_eff, :]
        L_small = L[:r_eff, :r_eff]
        S_diff = (L_small @ S_r.unsqueeze(-1)).squeeze()
        if S_diff.dim() == 0: S_diff = S_diff.unsqueeze(0)
        return lambda_diff * (U_r @ torch.diag(S_diff) @ Vh_r)

    def step(self, novelty_signal: float, closure=None):
        for group in self.param_groups:
            for p in group['params']:
                if p.grad is None: continue
                grad = p.grad.data
                decay = group['gamma'] * p.data
                advection = group['alpha'] * novelty_signal * torch.sign(p.data)

                diffusion = torch.zeros_like(p.data)
                if self.L_mean is not None and p.data.dim() == 2:
                    diffusion = self._semantic_diffusion(p.data, self.L_mean, group['lambda_diff'])

                # Ecuación Disipativa
