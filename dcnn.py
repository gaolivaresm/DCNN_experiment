# ── Celda 3: PrigogineOptimizer ───────────────────────────────────────────────
class PrigogineOptimizer(Optimizer):
    """
    Optimizador termodinámico con difusión semántica auto-referencial.

    Ecuación de actualización (Prigogine discreto):
        w_{t+1} = w_t - η∇L - γw_t + αN(x) + λ·Δ_sem(w_t)

    Donde:
        -η∇L          : aprendizaje estándar (backprop)
        -γw_t         : decaimiento metabólico (perecimiento)
        +αN(x)        : inyección de novedad (advección estabilizada)
        +λ·Δ_sem(w_t) : difusión topológica via Laplaciano de atención
    """

    def __init__(self, params, lr=1e-5, gamma=1e-4, alpha=0.01, lambda_diff=0.005):
        """
        Args:
            lr          : tasa de aprendizaje base
            gamma       : tasa de decaimiento (perecimiento basal)
            alpha       : multiplicador de novedad (fuerza de concrescencia)
            lambda_diff : coeficiente de difusión (estructura disipativa)
        """
        defaults = dict(lr=lr, gamma=gamma, alpha=alpha, lambda_diff=lambda_diff)
        super().__init__(params, defaults)
        self._attention_cache: dict = {}   # head_id → (L, A)
        self._prev_A: dict = {}            # head_id → A_{t-1} para KL

    # ── Registro de atención desde hooks externos ──────────────────────────────
    def register_attention(self, head_id: str, attn_weights: torch.Tensor):
        """
        Llamar desde forward hooks en cada BertSelfAttention.
        attn_weights: (batch, heads, seq, seq)
        """
        A = attn_weights.detach().mean(dim=(0, 1))   # (seq, seq)
        A = 0.5 * (A + A.T)                          # simetrizamos
        D = torch.diag(A.sum(dim=1))
        L = D - A                                    # Laplaciano combinatorio
        self._attention_cache[head_id] = (L, A)

    # ── Señal de novedad: KL divergence entre A_t y A_{t-1} ──────────────────
    def compute_novelty(self, eps: float = 1e-8) -> float:
        """
        KL(A_t || A_{t-1}) promediado sobre cabezales registrados.
        Alta novedad → el modelo exploró conexiones conceptuales nuevas.
        Primer paso siempre = 0.0 (sin historial previo).
        """
        kl_total = 0.0
        count = 0
        for hid, (_, A_curr) in self._attention_cache.items():
            if hid in self._prev_A:
                A_prev    = self._prev_A[hid].clamp(min=eps)
                A_c       = A_curr.clamp(min=eps)
                kl_total += (A_c * (A_c / A_prev).log()).sum().item()
                count += 1
        return kl_total / max(count, 1)

    # ── Difusión semántica via Laplaciano de grafo de atención ────────────────
    @staticmethod
    def _semantic_diffusion(W: torch.Tensor,
                             L: torch.Tensor,
                             lambda_diff: float) -> torch.Tensor:
        """
        Difunde W a lo largo del grafo de prehensión (grafo de atención).

        W : (d_model, d_model) — matriz de proyección Q/K/V
        L : (seq, seq)         — Laplaciano del grafo de atención

        La topología de difusión NO se impone desde fuera; emerge de los
        propios actos de concrescencia del modelo en cada forward pass.
        """
        try:
            U, S, Vh = torch.linalg.svd(W, full_matrices=False)
        except torch.linalg.LinAlgError:
            return torch.zeros_like(W)

        # Alinear dimensiones: r_eff = min(rango de W, tamaño de L)
        r_eff   = min(S.shape[0], L.shape[0])
        U_r     = U[:, :r_eff]
        S_r     = S[:r_eff]
        Vh_r    = Vh[:r_eff, :]
        L_small = L[:r_eff, :r_eff]

        S_diff = (L_small @ S_r.unsqueeze(-1)).squeeze()

        # Garantizar que S_diff sea 1-D para torch.diag
        if S_diff.dim() == 0:
            S_diff = S_diff.unsqueeze(0)

        return lambda_diff * (U_r @ torch.diag(S_diff) @ Vh_r)

    # ── Paso de optimización ──────────────────────────────────────────────────
    def step(self, novelty_signal: float = None, closure=None):
        loss = None
        if closure is not None:
            loss = closure()

        # Novedad: calcular ANTES de llamar step() para logging correcto
        if novelty_signal is None:
            novelty_signal = self.compute_novelty()

        # Laplaciano promedio sobre todos los cabezales registrados
        L_mean = None
        if self._attention_cache:
            L_mean = torch.stack(
                [v[0] for v in self._attention_cache.values()]
            ).mean(dim=0)

        for group in self.param_groups:
            lr          = group['lr']
            gamma       = group['gamma']
            alpha       = group['alpha']
            lambda_diff = group['lambda_diff']

            for p in group['params']:
                if p.grad is None:
                    continue

                grad      = p.grad.data

                # 1. Decaimiento metabólico — el perecer
                decay     = gamma * p.data

                # 2. Inyección de novedad — concrescencia
                advection = alpha * novelty_signal * torch.sign(p.data)

                # 3. Difusión topológica — estructura disipativa
                diffusion = (
                    self._semantic_diffusion(p.data, L_mean, lambda_diff)
                    if L_mean is not None and p.data.dim() == 2
                    else torch.zeros_like(p.data)
                )

                # ── Ecuación de Prigogine ──────────────────────────────────
                p.data = p.data - lr * grad - decay + advection + diffusion

        # Rotar cache: A_t → A_{t-1} para el próximo paso
        self._prev_A = {hid: v[1] for hid, v in self._attention_cache.items()}
        self._attention_cache.clear()
        return loss


# ── Celda 4: hooks de atención ────────────────────────────────────────────────
def attach_hooks(model: BertModel, optimizer: PrigogineOptimizer) -> list:
    """
    Registra forward hooks en cada BertSelfAttention.
    Los hooks alimentan el Laplaciano semántico del optimizador.
    """
    hooks = []
    for i, layer in enumerate(model.encoder.layer):
        def make_hook(idx):
            def hook(module, inp, out):
                if isinstance(out, tuple) and len(out) > 1:
                    attn_weights = out[1]
                    if attn_weights is not None:
                        optimizer.register_attention(f"L{idx}", attn_weights)
            return hook
        hooks.append(
            layer.attention.self.register_forward_hook(make_hook(i))
        )
    return hooks


