def infer(text: str, top_k: int = 5):
    """
    Devuelve embedding CLS y los tokens más prehendidos
    (aquellos que reciben mayor atención acumulada).
    """
    enc = tokenizer(text, return_tensors='pt',
                    truncation=True, max_length=64,
                    padding='max_length').to(device)

    with torch.no_grad():
        out = model(**enc, output_attentions=True)

    # Embedding global del texto
    cls_emb = out.last_hidden_state[:, 0, :]              # (1, 768)

    # Atención promedio sobre las 12 capas y 12 cabezales
    if out.attentions is None:
        print(f"Warning: out.attentions is None for text: '{text[:50]}...'\n")
        tokens = tokenizer.convert_ids_to_tokens(enc['input_ids'][0])
        # Create a dummy token_importance (e.g., uniform distribution) or handle as error
        token_importance = torch.ones(len(tokens), device=device) / len(tokens)
        return cls_emb, token_importance, tokens

    # FIX: Filter out None values from out.attentions before stacking
    valid_attentions = [attn for attn in out.attentions if attn is not None]

    if not valid_attentions:
        print(f"Warning: No valid attention weights found for text: '{text[:50]}...'\n")
        tokens = tokenizer.convert_ids_to_tokens(enc['input_ids'][0])
        # Create a dummy token_importance (e.g., uniform distribution) or handle as error
        token_importance = torch.ones(len(tokens), device=device) / len(tokens)
        return cls_emb, token_importance, tokens

    attn_stack       = torch.stack(valid_attentions)      # (num_valid_layers, batch, heads, seq, seq)
    attn_mean        = attn_stack.mean(dim=(0, 1, 2))     # (seq, seq)
    token_importance = attn_mean.sum(dim=0)               # (seq,)
    token_importance = token_importance / token_importance.sum()

    tokens  = tokenizer.convert_ids_to_tokens(enc['input_ids'][0])
    top_idx = token_importance.topk(min(top_k, len(tokens))).indices

    print(f"\n{'─'*58}")
    print(f"Texto : {text}")
    print(f"{'─'*58}")
    print(f"Norma CLS : {cls_emb.norm().item():.4f}")
    print(f"\nTokens más prehendidos (atención acumulada):")
    for i in top_idx:
        print(f"  [{i:2d}] '{tokens[i]:<18}' → {token_importance[i].item():.4f}")

    return cls_emb, token_importance, tokens


