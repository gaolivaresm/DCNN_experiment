    # Calcular la similaridad coseno entre los dos textos diferentes
    sim = F.cosine_similarity(embeddings[0], embeddings[1]).item()

    # Si la similaridad es muy alta (>0.95), penalizamos el score fuertemente
    penalty = 0.0
    if sim > 0.95:
        penalty = 1000.0 * sim  # Castigo por pandeo estructural

    # El Score final que Optuna intentará minimizar
    score = (total_loss / len(calibration_texts)) + penalty

    return score

