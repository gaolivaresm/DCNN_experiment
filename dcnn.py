# --- Prigogine Optimizer Class (The Dynamic Heart of the System) ---
# This optimizer treats neural network weights as a fluid, subject to dynamic forces.
class PrigogineOptimizerCausal(Optimizer):
    """
    A thermodynamic optimizer adapted for causal models (like GPT-2) that operates
    without explicit forward hooks, computing novelty and diffusion directly from attention outputs.
    
    Update Equation (Discrete Prigogine Equation):
        w_{t+1} = w_t - η∇L - γw_t + αN(x) + λ·Δ_sem(w_t)

    Where:
        - η∇L: Standard learning (backpropagation)
        - -γw_t: Metabolic decay (weight 'perishing')
        - +αN(x): Novelty injection (stabilized advection)
        - +λ·Δ_sem(w_t): Topological diffusion via attention Laplacian
    """
    def __init__(self, params, lr=2e-5, gamma=1e-6, alpha=1.58e-4, lambda_diff=1.12e-3):
        """
        Args:
            params      : Iterable of parameters to optimize.
            lr          : Base learning rate.
            gamma       : Decay rate (basal perishing).
            alpha       : Novelty multiplier (force of 'concrescence').
            lambda_diff : Diffusion coefficient (dissipative structure formation).
        """
        defaults = dict(lr=lr, gamma=gamma, alpha=alpha, lambda_diff=lambda_diff)
        super().__init__(params, defaults)
        self._prev_A = {}  # Cache for previous attention matrices to compute novelty
        self.L_mean = None # Placeholder for mean Laplacian, computed in compute_novelty

    def compute_novelty(self, attentions, eps=1e-8) -> float:
        """
        Computes novelty as KL divergence between current and previous attention matrices.
        High novelty indicates the model explored new conceptual connections.
        The first step always yields 0.0 as there's no prior history.
        """
        kl_total = 0.0
        count = 0
        L_list = [] # List to store Laplacians for averaging
        
        # Filter out any None attention weights, which can occur for various reasons
        valid_attentions = [a for a in attentions if a is not None]
        if not valid_attentions:
            return 0.0 # Return no novelty if no valid attentions are present
            
        for idx, attn in enumerate(valid_attentions):
            # Attention shape: (batch, heads, seq, seq). Average across batch and heads.
            A_curr = attn.detach().mean(dim=(0, 1)) 
            
            # Construct the symmetric attention matrix and its combinatorial Laplacian
            A_sym = 0.5 * (A_curr + A_curr.T)
            D = torch.diag(A_sym.sum(dim=1))
            L_list.append(D - A_sym)
            
            # Compute KL divergence if a previous attention matrix exists for this layer
            if idx in self._prev_A:
                # Align dimensions: sequence length grows by one token each step
                seq_len = self._prev_A[idx].shape[0]
                A_c_cut = A_curr[:seq_len, :seq_len].clamp(min=eps) # Crop current A and clamp for log stability
                A_p = self._prev_A[idx].clamp(min=eps) # Clamp previous A for log stability
                
                kl_total += (A_c_cut * (A_c_cut / A_p).log()).sum().item()
                count += 1
            
            self._prev_A[idx] = A_curr # Update previous attention cache

        # Compute the mean Laplacian from all valid attention heads
        self.L_mean = torch.stack(L_list).mean(dim=0) if L_list else None
        return kl_total / max(count, 1) # Return average KL novelty

    @staticmethod
    def _semantic_diffusion(W: torch.Tensor, L: torch.Tensor, lambda_diff: float) -> torch.Tensor:
        """
        Applies semantic diffusion to weights (W) based on the attention graph's Laplacian (L).
        This simulates a dissipative structure where weight changes propagate through semantic connections.
        """
        try:
            U, S, Vh = torch.linalg.svd(W, full_matrices=False)
        except torch.linalg.LinAlgError: # Handle cases where SVD might fail (e.g., non-square matrices)
            return torch.zeros_like(W)
        
        # Align dimensions for matrix multiplication with Laplacian
        r_eff = min(S.shape[0], L.shape[0])
        U_r, S_r, Vh_r = U[:, :r_eff], S[:r_eff], Vh[:r_eff, :]
        L_small = L[:r_eff, :r_eff]
        
        # Diffuse the singular values using the Laplacian
        S_diff = (L_small @ S_r.unsqueeze(-1)).squeeze()
        if S_diff.dim() == 0: S_diff = S_diff.unsqueeze(0) # Ensure S_diff is 1-D for torch.diag
        
        return lambda_diff * (U_r @ torch.diag(S_diff) @ Vh_r)

    def step(self, novelty_signal: float, closure=None):
        """
        Performs a single optimization step, applying the Prigogine equation to model parameters.
        """
        for group in self.param_groups:
            for p in group['params']:
                if p.grad is None: continue # Skip parameters without gradients
                
                grad = p.grad.data
                decay = group['gamma'] * p.data # Metabolic decay term
                advection = group['alpha'] * novelty_signal * torch.sign(p.data) # Novelty injection term
                
                diffusion = torch.zeros_like(p.data)
                # Apply semantic diffusion only if a mean Laplacian is available and parameter is 2D
                if self.L_mean is not None and p.data.dim() == 2:
                    diffusion = self._semantic_diffusion(p.data, self.L_mean, group['lambda_diff'])
                
                # The Prigogine Equation: update weights based on all terms
                p.data = p.data - (group['lr'] * grad) - decay + advection + diffusion


